import UIKit

extension UIColor{
    
    static func blackColor() -> UIColor{
        return .black
    }
    
    static func grayColor() -> UIColor{
        return .gray
    }
    
    static func whiteColor() -> UIColor{
        return .white
    }
    
    static func backgroundColor() -> UIColor{
        return .white
    }
}
